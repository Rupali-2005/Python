# Demonstrates for and while loops basics

# Using for loop to print numbers 1 to 5
print("Using for loop:")
for i in range(1, 6):
    print(i, end=' ')
print("\n")  # New line

# Using while loop to print numbers 1 to 5
print("Using while loop:")
count = 1
while count <= 5:
    print(count, end=' ')
    count += 1
print()
