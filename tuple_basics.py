# Demonstrates tuple creation and basic operations

# Create a tuple
colors = ('red', 'green', 'blue')

print("Tuple:", colors)

# Access elements by index
print("First color:", colors[0])
print("Last color:", colors[-1])

# Tuples are immutable, but can count and find index
print("Count of 'red' in tuple:", colors.count('red'))
print("Index of 'green' in tuple:", colors.index('green'))
